import { Share2 } from "lucide-react";
import { Button } from "@/components/ui/button";
import { useToast } from "@/hooks/use-toast";

interface ImageHeaderProps {
  creator: {
    name: string;
    avatar: string;
  };
  timeAgo: string;
  imageUrl?: string;
  imageId?: number;
}

export const ImageHeader = ({ creator, timeAgo, imageUrl, imageId }: ImageHeaderProps) => {
  const { toast } = useToast();

  const handleShare = async () => {
    if (!imageUrl || !imageId) {
      toast({
        title: "Error",
        description: "No image available to share",
        variant: "destructive",
      });
      return;
    }

    // Construct the shareable URL for the image card
    const shareableUrl = `${window.location.origin}/marketplace?image=${imageId}`;

    try {
      // First try to use the clipboard API as it's more widely supported
      await navigator.clipboard.writeText(shareableUrl);
      
      // Then attempt to use the Web Share API if available
      if (navigator.share) {
        try {
          await navigator.share({
            title: `Generated by ${creator.name}`,
            text: 'Check out this generated image!',
            url: shareableUrl
          });
          toast({
            title: "Success",
            description: "Image shared successfully",
          });
        } catch (shareError) {
          // If share fails but we already copied to clipboard, show that message
          if ((shareError as Error).name === 'AbortError') {
            // User cancelled sharing, but clipboard copy succeeded
            toast({
              title: "Success",
              description: "Link copied to clipboard - you can now paste and share it",
            });
          } else {
            // Some other sharing error occurred
            console.error('Share error:', shareError);
            toast({
              title: "Info",
              description: "Link copied to clipboard (sharing unavailable)",
            });
          }
        }
      } else {
        // Web Share API not available, but clipboard copy succeeded
        toast({
          title: "Success",
          description: "Link copied to clipboard - you can now paste and share it",
        });
      }
    } catch (error) {
      console.error('Share error:', error);
      toast({
        title: "Error",
        description: "Unable to share or copy link. Please try again.",
        variant: "destructive",
      });
    }
  };

  return (
    <div className="flex items-center justify-between mb-2">
      <div className="flex items-center space-x-2">
        <img
          src={creator.avatar}
          alt={creator.name}
          className="w-6 h-6 rounded-full"
        />
        <span className="text-sm font-medium">{creator.name}</span>
        <span className="text-sm text-gray-400">{timeAgo}</span>
      </div>
      <Button 
        variant="ghost" 
        size="sm" 
        className="p-1"
        onClick={handleShare}
      >
        <Share2 className="h-4 w-4" />
      </Button>
    </div>
  );
};