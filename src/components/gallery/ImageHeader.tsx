import { Share2 } from "lucide-react";
import { Button } from "@/components/ui/button";
import { useToast } from "@/hooks/use-toast";

interface ImageHeaderProps {
  creator: {
    name: string;
    avatar: string;
  };
  timeAgo: string;
  imageUrl?: string;
  imageId?: number;
}

export const ImageHeader = ({ creator, timeAgo, imageUrl, imageId }: ImageHeaderProps) => {
  const { toast } = useToast();

  const handleShare = async () => {
    if (!imageUrl || !imageId) {
      toast({
        title: "Error",
        description: "No image available to share",
        variant: "destructive",
      });
      return;
    }

    // Construct the shareable URL for the image card
    const shareableUrl = `${window.location.origin}/marketplace?image=${imageId}`;

    try {
      if (navigator.canShare && navigator.canShare({ url: shareableUrl })) {
        try {
          await navigator.share({
            title: 'Check out this generated image!',
            text: `Generated by ${creator.name}`,
            url: shareableUrl
          });
          toast({
            title: "Success",
            description: "Image shared successfully",
          });
        } catch (shareError) {
          if ((shareError as Error).name !== 'AbortError') {
            // Only show error if it wasn't a user cancellation
            await navigator.clipboard.writeText(shareableUrl);
            toast({
              title: "Info",
              description: "Image URL copied to clipboard instead",
            });
          }
        }
      } else {
        // If Web Share API is not available or content cannot be shared
        await navigator.clipboard.writeText(shareableUrl);
        toast({
          title: "Success",
          description: "Image URL copied to clipboard",
        });
      }
    } catch (error) {
      console.error('Share error:', error);
      toast({
        title: "Error",
        description: "Failed to share or copy image URL",
        variant: "destructive",
      });
    }
  };

  return (
    <div className="flex items-center justify-between mb-2">
      <div className="flex items-center space-x-2">
        <img
          src={creator.avatar}
          alt={creator.name}
          className="w-6 h-6 rounded-full"
        />
        <span className="text-sm font-medium">{creator.name}</span>
        <span className="text-sm text-gray-400">{timeAgo}</span>
      </div>
      <Button 
        variant="ghost" 
        size="sm" 
        className="p-1"
        onClick={handleShare}
      >
        <Share2 className="h-4 w-4" />
      </Button>
    </div>
  );
};